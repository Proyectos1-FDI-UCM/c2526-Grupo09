    public void ConfirmSleep()
    {
        GameManager.Instance.Sleep();
        panelDormir.SetActive(false);
        Debug.Log("Día pasado correctamente. Ya puedes salir de casa.");
    }

    public void CancelSleep()
    {
        panelDormir.SetActive(false);
        Debug.Log("No pasamos de dia");
    }

