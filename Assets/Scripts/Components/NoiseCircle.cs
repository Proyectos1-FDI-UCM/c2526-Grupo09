    [SerializeField]
    private float Speed = 1.0f;  // velocidad de aumento de tamaño

    [SerializeField]
    private Vector3 PosFinal = new Vector3(5, 5, 5);  // posición final del círculo

    #endregion

    // ---- ATRIBUTOS PRIVADOS ----
    #region Atributos Privados (private fields)
